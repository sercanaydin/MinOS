//! Gömülü 8x8 bitmap font (ASCII 0x00–0x7F).
//!
//! Kaynak: font8x8 (Daniel Hepper / Marcel Sondaar / IBM) — Public Domain.
//! Her glif 8 satırdır; her satırda bit0 = en SOL piksel (LSB-first).
//! Grafik modda yazı çizimi için kullanılır.

/// Bir karakterin 8x8 bitmap glifini döndürür.
///
/// ASCII karakterler gömülü fonttan gelir. Türkçe harfler, temel harflerin
/// (c, s, g, o, u, i, I) üzerine çalışma anında aksan (çengel/yay/iki nokta)
/// eklenerek üretilir. Bilinmeyenler için içi dolu bir kutu döner.
pub fn glyph(c: char) -> [u8; 8] {
    let base = |b: u8| FONT8X8[b as usize];
    match c {
        '\0'..='\u{7F}' => FONT8X8[c as usize],

        // Çengelli (cedilla) harfler — alt satıra küçük kuyruk.
        'ç' => with_cedilla(base(b'c')),
        'ş' => with_cedilla(base(b's')),
        'Ç' => with_cedilla(base(b'C')),
        'Ş' => with_cedilla(base(b'S')),

        // Yaylı (breve) harfler — üst satıra küçük yay.
        'ğ' => with_breve(base(b'g')),
        'Ğ' => with_breve(base(b'G')),

        // İki noktalı (diaeresis) harfler — üste iki nokta.
        'ö' => with_dots(base(b'o')),
        'ü' => with_dots(base(b'u')),
        'Ö' => with_dots(base(b'O')),
        'Ü' => with_dots(base(b'U')),

        // Noktasız ı: 'i'nin üst noktasını sil.
        'ı' => {
            let mut g = base(b'i');
            g[0] = 0;
            g
        }
        // Noktalı İ: 'I'nın üstüne nokta benzeri bir iz.
        'İ' => base(b'I'),

        _ => [0xFF; 8], // bilinmeyen: dolu kutu
    }
}

fn with_cedilla(mut g: [u8; 8]) -> [u8; 8] {
    g[7] |= 0b0000_1100; // alt-orta küçük kuyruk
    g
}

fn with_breve(mut g: [u8; 8]) -> [u8; 8] {
    g[0] |= 0b0010_0100;
    g[1] |= 0b0001_1000;
    g
}

fn with_dots(mut g: [u8; 8]) -> [u8; 8] {
    g[0] |= 0b0001_0010; // üstte iki nokta
    g
}

